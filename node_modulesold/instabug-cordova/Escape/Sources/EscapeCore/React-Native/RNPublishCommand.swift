import Foundation
import SwiftCLI
import SwiftShell
import FileUtils
import PerfectMarkdown

class RNPublishCommand: Command {
    let name = "publish"
    let shortDescription = "Publish the react native Instabug library to npm, creates a release on Github along with the changelog and updates the dashboard's changelog. The following environment variables are required for the command to run: RELEASE_GITHUB_TOKEN, DASHBOARD_TOKEN, NPM_TOKEN"
    
    func execute() {
        do {
            try GitHub.publishRelease(repoAt: .reactNative, versionFrom: .npm)
            try Instabug.publishToDashboard(repoAt: .reactNative, versionFrom: .npm, forTargetOS: .reactNative, withVersionNumber: nil)
            try PackageManager.publishToNpm()
        } catch {
            print(error)
            exit(1)
        }
    }
}
